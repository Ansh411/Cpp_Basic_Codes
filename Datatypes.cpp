#include<bits/stdc++.h>
using namespace std;
int main () {
    int a=1; //Integer
    cout<<a<<endl;
    float b=2.43; // Floating Point Number 
    cout<<b<<endl;
    char ch=99; // Character
    cout<<ch<<endl;
    bool a1=false; // Boolean
    bool b1=true;
    cout<<a1<<endl;
    cout<<b1<<endl;
    double d = 7.8/5; // Double
    cout<<d<<endl;
    short s=5;
    cout<<s<<endl;
    cout<<"Size of int"<<" "<<sizeof(a)<<endl;
    cout<<"Size of float"<<" "<<sizeof(b)<<endl;
    cout<<"Size of char"<<" "<<sizeof(ch)<<endl;
    cout<<"Size of bool"<<" "<<sizeof(a1)<<endl;
    cout<<"Size of bool"<<" "<<sizeof(b1)<<endl;
    cout<<"Size of double"<<" "<<sizeof(d)<<endl;




}