#include<bits/stdc++.h>
using namespace std;
int main(){
    int n;
    cout<<"Enter n :"<<endl;
    cin>>n;
    int i=1;
    cout<<"Pattern :"<<endl;
    while(i<=n){
     int j=1;
     while (j<=n)
     {
        cout<<"*";
        j++;
     }
        cout<<endl;
        i++;
    }

}
